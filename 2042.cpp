#include "problem.h"
#include <iostream>
using namespace std;

int getSheep(int n) {
	if (n == 0)
		return 3;
	return (getSheep(n - 1) - 1) * 2;
}

void problem2042() {
	int n, m;
	while (cin >> n) {
		while (n--) {
			cin >> m;
			cout << getSheep(m) << endl;
		}
	}
}